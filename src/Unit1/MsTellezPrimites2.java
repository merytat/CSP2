package Unit1;

public class MsTellezPrimites2 {
    public static void main(String[] args) {
        //instantiate variables
        int x = 2;
        int y = 10;

        //output
        //Concatenate output using +
        System.out.println("x + y = " + (x+y));
        System.out.println("x - y = " + (x-y));
        System.out.println("x * y = " + x*y);
        System.out.println("x / y = " + (double)x/y);
    }
}
